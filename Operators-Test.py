num1 = 4
num2 = 2.50

sum_result = num1 + num2
print("Sum of two numbers:", sum_result)

subtract_result = num1 - num2
print("Difference of two numbers:", subtract_result)

multiply_result = num1 * num2
print("Product of two numbers:", multiply_result)

divide_result = num1 / num2
print("Division of two numbers:", divide_result)

modulus_result = num1 % num2
print("Modulus of two numbers:", modulus_result)

exponent_result = num1 ** num2
print("Exponentiation of two numbers:", exponent_result)

floor_division_result = num1 // num2
print("Floor division of two numbers:", floor_division_result)


print("type (num1):", type(num1))
print("type (num2):", type(num2))
print("type (sum_result):", type(sum_result))

float( num1)
