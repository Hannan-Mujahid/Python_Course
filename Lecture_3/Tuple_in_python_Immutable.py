            # Tuple in Python - Immutable with slicing 

tuple = (1,2,3,4,5,8,5,3)
print(tuple[0])
print(tuple[-1])
print(tuple[:3])  # First three elements
print(tuple[-3:])  # Last three elements
print(tuple[1:4])  # Elements from index 1 to 3


